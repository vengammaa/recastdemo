package com.lti.recast.commons.reportvalueobjects;

public class PromptElement {

	
	String promptName;
	String ismultiSelect ;
	String paraName ;
	String refQuery;
	String isReq;
	String selectValUI ;
	String dispName;
	String colName;
	private String promptType;
	
	public String getPromptName() {
		return promptName;
	}
	public void setPromptName(String promptName) {
		this.promptName = promptName;
	}
	public String getIsmultiSelect() {
		return ismultiSelect;
	}
	public void setIsmultiSelect(String ismultiSelect) {
		this.ismultiSelect = ismultiSelect;
	}
	public String getParaName() {
		return paraName;
	}
	public void setParaName(String paraName) {
		this.paraName = paraName;
	}
	public String getRefQuery() {
		return refQuery;
	}
	public void setRefQuery(String refQuery) {
		this.refQuery = refQuery;
	}
	public String getIsReq() {
		return isReq;
	}
	public void setIsReq(String isReq) {
		this.isReq = isReq;
	}
	public String getSelectValUI() {
		return selectValUI;
	}
	public void setSelectValUI(String selectValUI) {
		this.selectValUI = selectValUI;
	}
	public String getDispName() {
		return dispName;
	}
	public void setDispName(String dispName) {
		this.dispName = dispName;
	}
	public String getColName() {
		return colName;
	}
	public void setColName(String colName) {
		this.colName = colName;
	}
	public String getPromptType() {
		return promptType;
	}
	public void setPromptType(String promptType) {
		this.promptType = promptType;
	}
}
